package cn.itganhuo.app.entity;

import java.io.Serializable;

/**
 * 当前端页面使用<code>autocomplete</code>插件时返回信息的封装类，其作用包括但不限于：
 * <ol>
 * <li>添加文章填写标签时将异步返回的标签进行封装</li>
 * </ol>
 * 
 * @version 0.0.1-SNAPSHOT
 * @author 深圳-小兴
 */
public class AutoComplete implements Serializable {

	private static final long serialVersionUID = -9138453413215126808L;

	/*
	 * ID
	 */
	private Integer id;
	/*
	 * 表现值
	 */
	private String label;
	/*
	 * 真实值
	 */
	private String value;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getLabel() {
		return label;
	}

	public void setLabel(String label) {
		this.label = label;
	}

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#hashCode()
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		return result;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#equals(java.lang.Object)
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		AutoComplete other = (AutoComplete) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		return true;
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "AutoComplete [id=" + id + ", label=" + label + ", value=" + value + "]";
	}

}
